using IdentityServer4.Test;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Claims;
using System.Threading.Tasks;
using static IdentityModel.OidcConstants;

namespace MyIdentityServer4
{
    public class Config
    {
        public static IEnumerable<Client> GetClients()
        {
            return new List<Client> {
                new Client {
                    ClientId = "oauthClient",
                    ClientName = "Example Client Credentials Client Application",
                    AllowedGrantTypes = new List<string> { OidcConstants.GrantTypes.ClientCredentials },
                    ClientSecrets = new List<Secret> {
                        new Secret("superSecretPassword".Sha256())},
                    AllowedScopes = new List<string> {"customAPI.read"}
                },
                new Client {
                    ClientId = "openIdConnectClient",
                    ClientName = "Example Implicit Client Application",
                    ClientUri = "http://localhost:54486",
                    AllowedGrantTypes = new List<string> { OidcConstants.GrantTypes.Implicit },
                    AllowedScopes = new List<string>
                    {
                        IdentityServerConstants.StandardScopes.OpenId,
                        IdentityServerConstants.StandardScopes.Profile,
                        IdentityServerConstants.StandardScopes.Email,
                        "role",
                        "customAPI.write"
                    },
                    AllowedCorsOrigins = { "http://localhost:54486" },

                    // where to redirect to after login
                    RedirectUris = new List<string> {"https://localhost:44350/signin-oidc"},

                    // where to redirect to after logout
                    PostLogoutRedirectUris = new List<string> { "https://localhost:44350/signout-callback-oidc" }
                }
